// while loop on js: while loop first checks the condition if true, then executes the code inside it.

let i = 1
while (i <= 10) {
    console.log(i);
    i++
}

// do-while loop on js: do-while loop first executes the code inside then checks the condition, if true then code will execute again. special thing of its is it will run the code once even the condition is false.
let j = 1
do {
    console.log(j);
    j++
} while (j <= 10);